# Generator Class
# =============================================================================

class WorkerGenerator:
    """
    Generate worker deployment files from config.
    
    Example:
        gen = WorkerGenerator("/path/to/config.json")
        gen.generate(output_dir="/path/to/deploy")
    """
    
    def __init__(self, config_path: str):
        """
        Initialize generator.
        
        Args:
            config_path: Path to config.json
        """
        self.config_path = os.path.expanduser(config_path)
        self.config = self._load_config()
    
    def _load_config(self) -> Dict[str, Any]:
        """Load configuration from file."""
        if not os.path.exists(self.config_path):
            raise FileNotFoundError(f"Config not found: {self.config_path}")
        
        with open(self.config_path, 'r', encoding='utf-8') as f:
            return json.load(f)
    
    def _get_default(self, key: str, default: Any = None) -> Any:
        """Get config value with default."""
        return self.config.get(key, default)
    
    def generate(
        self,
        output_dir: str = ".",
        include_systemd: bool = True,
        include_management: bool = True
    ) -> List[str]:
        """
        Generate all worker deployment files.
        
        Args:
            output_dir: Output directory for generated files
            include_systemd: Generate systemd service file
            include_management: Generate management script
            
        Returns:
            List of generated file paths
        """
        output_dir = os.path.expanduser(output_dir)
        os.makedirs(output_dir, exist_ok=True)
        
        generated_files = []
        
        # 1. Generate worker.py
        worker_path = self._generate_worker_py(output_dir)
        generated_files.append(worker_path)
        
        # 2. Generate systemd service (if root)
        if include_systemd:
            service_path = self._generate_systemd_service(output_dir)
            generated_files.append(service_path)
        
        # 3. Generate management script
        if include_management:
            script_path = self._generate_management_script(output_dir)
            generated_files.append(script_path)
        
        return generated_files
    
    def _generate_worker_py(self, output_dir: str) -> str:
        """Generate worker.py script."""
        import datetime
        
        output_path = os.path.join(output_dir, "worker.py")
        
        content = WORKER_TEMPLATE.format(
            config_path=self.config_path,
            generated_at=datetime.datetime.now().isoformat(),
            default_config=self.config_path,
            default_log_level=self._get_default("log_level", "INFO"),
            log_file=self._get_default("log_file", "~/.EITElite/worker.log")
        )
        
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(content)
        
        # Make executable
        os.chmod(output_path, 0o755)
        
        print(f"Generated: {output_path}")
        return output_path
    
    def _generate_systemd_service(self, output_dir: str) -> str:
        """Generate systemd service file."""
        service_dir = "/etc/systemd/system"
        service_name = f"tical-worker-{self.config.get('name', 'default')}.service"
        output_path = os.path.join(output_dir, service_name)
        
        content = SYSTEMD_SERVICE_TEMPLATE.format(
            worker_name=self.config.get('name', 'default'),
            user=self._get_default("systemd_user", "ubuntu"),
            work_dir=self._get_default("work_dir", os.path.dirname(os.path.dirname(output_dir))),
            python_path=sys.executable,
            worker_script=os.path.join(output_dir, "worker.py"),
            config_path=self.config_path,
            api_key_env=self._get_default("api_key_env", "GOOGLE_API_KEY"),
            memory_max=self._get_default("memory_max", "512M"),
            cpu_quota=self._get_default("cpu_quota", "100%")
        )
        
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"Generated: {output_path}")
        print(f"To install: sudo cp {output_path} {service_dir}/ && sudo systemctl daemon-reload")
        return output_path
    
    def _generate_management_script(self, output_dir: str) -> str:
        """Generate management script."""
        output_path = os.path.join(output_dir, f"{self.config.get('name', 'worker')}_manage.sh")
        
        content = MANAGEMENT_SCRIPT_TEMPLATE.format(
            worker_name=self.config.get('name', 'default'),
            worker_script=os.path.join(output_dir, "worker.py"),
            config_path=self.config_path,
            log_file=self._get_default("log_file", "~/.EITElite/worker.log")
        )
        
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(content)
        
        # Make executable
        os.chmod(output_path, 0o755)
        
        print(f"Generated: {output_path}")
        return output_path


