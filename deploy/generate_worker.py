def main():
    """CLI entry point."""
    import argparse
    
    parser = argparse.ArgumentParser(
        description="Generate worker deployment files from config"
    )
    parser.add_argument(
        "config",
        type=str,
        help="Path to config.json"
    )
    parser.add_argument(
        "-o", "--output",
        type=str,
        default=".",
        help="Output directory (default: current directory)"
    )
    parser.add_argument(
        "--no-systemd",
        action="store_true",
        help="Skip systemd service file generation"
    )
    parser.add_argument(
        "--no-management",
        action="store_true",
        help="Skip management script generation"
    )
    
    args = parser.parse_args()
    
    try:
        gen = WorkerGenerator(args.config)
        files = gen.generate(
            output_dir=args.output,
            include_systemd=not args.no_systemd,
            include_management=not args.no_management
        )
        
        print("\n" + "=" * 60)
        print("Generation complete!")
        print("=" * 60)
        print(f"Generated {len(files)} files:")
        for f in files:
            print(f"  - {f}")
        
    except Exception as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
