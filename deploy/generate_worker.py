MANAGEMENT_SCRIPT_TEMPLATE = '''#!/bin/bash
# EITElite Worker Management Script
# Generated by generate_worker.py

set -e

WORKER_NAME="{worker_name}"
WORKER_SCRIPT="{worker_script}"
CONFIG_PATH="{config_path}"
PID_FILE="/run/EITElite/{worker_name}.pid"
LOG_FILE="{log_file}"

# Colors
RED='\\033[0;31m'
GREEN='\\033[0;32m'
YELLOW='\\033[1;33m'
NC='\\033[0m' # No Color

log_info() {{
    echo -e "${{GREEN}}[INFO]${{NC}} $1"
}}

log_warn() {{
    echo -e "${{YELLOW}}[WARN]${{NC}} $1"
}}

log_error() {{
    echo -e "${{RED}}[ERROR]${{NC}} $1"
}}

check_root() {{
    if [ "$EUID" -ne 0 ]; then
        log_error "Please run as root (for systemd service management)"
        exit 1
    fi
}}

start() {{
    log_info "Starting {worker_name}..."
    check_root
    systemctl start "tical-worker-{worker_name}.service"
    log_info "Worker started"
}}

stop() {{
    log_info "Stopping {worker_name}..."
    check_root
    systemctl stop "tical-worker-{worker_name}.service"
    log_info "Worker stopped"
}}

restart() {{
    log_info "Restarting {worker_name}..."
    stop
    sleep 2
    start
}}

status() {{
    systemctl status "tical-worker-{worker_name}.service" --no-pager
}}

logs() {{
    journalctl -u "tical-worker-{worker_name}.service" -f "$@"
}}

case "$1" in
    start)
        start
        ;;
    stop)
        stop
        ;;
    restart)
        restart
        ;;
    status)
        status
        ;;
    logs)
        shift
        logs "$@"
        ;;
    *)
        echo "Usage: $0 {{start|stop|restart|status|logs}}"
        exit 1
        ;;
esac
'''


